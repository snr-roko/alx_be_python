# Declaring two variables containing two numbers to operate on
number1 = 10
number2 = 5

# Adding the two numbers
print("Addition of 10 and 5 is " + str(number1 + number2))

# Subtracting the two numbers
print("Subtraction of 10 and 5 is " + str(number1 - number2))

# Multiplying the two numbers
print("Multiplication of 10 and 5 is " + str(number1 * number2))
